fun main(args: Array<String>){
    var a:Double = 7.0
    var b:Double = 3.0


//    1. (+Addition)
    var add = a + b
    println("Addition: ${add}")

//    2. (- subtraction)
    var sub = a - b
    println("Subtraction: ${sub}")

//3. (* Multiplication)
    var multi = a*b
    println("Multiplication: ${multi}")

//    4.(/ Division)
    var div = a/b
    println(" Division: ${div}")

//    5. (% Modulus)
    var mul = a%b
    println("Modulus: ${mul}")
}