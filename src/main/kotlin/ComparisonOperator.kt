fun main(args:Array<String>){
    var a = 10
    var b = 15

//    1. > (greater than)
    println(a>b)
//    2. < (Less than)
    println(a<b)
//    3. >= (Greater than equal)
    println(a >= b)
//    4. <= (Less than equal)
    println(a>=b)
//    5. == (equal)
    println(a==b)
//    6. !== (not equal)
    println(a != b)

}